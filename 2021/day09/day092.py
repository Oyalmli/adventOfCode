def inGrid(grid, point):
    y,x = point
    return 0 <= x < len(grid[0]) and 0 <= y < len(grid)

def goTo(visited, grid, q):
    point = q.pop()
    if point in visited: return 0
    visited.add(point)
    y,x = point
    deltas = [(-1, 0), (0, 1), (1, 0), (0, -1)]
    neighs = [(y+dy, x+dx) for (dy,dx) in deltas 
        if inGrid(grid, (y+dy, x+dx)) and grid[y][x] < grid[y+dy][x+dx] < 9]
    s = 1 + sum(map(lambda neigh_pos: goTo(visited, grid, neigh_pos), neighs))
    return s

